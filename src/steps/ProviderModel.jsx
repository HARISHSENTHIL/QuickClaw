export default function ProviderModel({ config, onChange, onNext, onBack }) {
  const selectedProvider = PROVIDERS.find((p) => p.id === config.provider)
  const isCustom = selectedProvider?.models === null

  const handleProviderClick = (p) => {
    const firstModel = p.models ? p.models[0] : ''
    onChange({ provider: p.id, model: firstModel })
  }

  const canContinue = Boolean(config.provider && config.model)

  return (
    <div className="pm-step">
      {/* Thin progress line at very top */}
      <div className="pm-progress-track">
        <div className="pm-progress-fill" />
      </div>

      {/* Drag header: Back ← · · · Step 1 of 2 */}
      <div className="pm-header">
        <button className="pm-back" onClick={onBack}>← Back</button>
        <span className="pm-badge">Step 1 of 2</span>
      </div>

      {/* Scrollable body */}
      <div className="pm-body">
        <h2 className="pm-title">Choose Provider &amp; Model</h2>
        <p className="pm-sub">Select the AI provider and model you want OctoClaw to use.</p>

        {/* 3 × 3 provider grid */}
        <div className="pm-grid">
          {PROVIDERS.map((p) => (
            <button
              key={p.id}
              className={`pm-card${config.provider === p.id ? ' pm-card-active' : ''}`}
              onClick={() => handleProviderClick(p)}
            >
              <div className="pm-card-icon" style={{ color: p.color }}>
                <p.Logo />
              </div>
              <div className="pm-card-info">
                <span className="pm-card-name">{p.label}</span>
                <span className="pm-card-desc">{p.desc}</span>
              </div>
            </button>
          ))}
        </div>

        {/* Model selector + CTA */}
        <div className="pm-bottom">
          <div className="pm-model-group">
            <label className="pm-model-label">Model</label>
            {isCustom ? (
              <input
                className="pm-model-input"
                placeholder={
                  config.provider === 'ollama'
                    ? 'e.g. llama3, mistral, phi3...'
                    : 'e.g. anthropic/claude-3-opus'
                }
                value={config.model}
                onChange={(e) => onChange({ model: e.target.value })}
                autoFocus
                spellCheck={false}
              />
            ) : (
              <select
                className="pm-model-select"
                value={config.model}
                onChange={(e) => onChange({ model: e.target.value })}
              >
                {selectedProvider?.models?.map((m) => (
                  <option key={m} value={m}>
                    {selectedProvider.modelLabels?.[m] || m}
                  </option>
                ))}
              </select>
            )}
          </div>

          <button className="pm-cta" onClick={onNext} disabled={!canContinue}>
            Continue
          </button>
        </div>
      </div>
    </div>
  )
}
